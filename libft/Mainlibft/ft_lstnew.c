#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}

int main()
{
	t_list *nazli;
	ft_lstnew(nazli);
	nazli -> content = "nazlii";
	printf("%s", &nazli);
}